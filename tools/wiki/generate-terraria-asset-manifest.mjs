import fs from "node:fs";
import path from "node:path";
import { pathToFileURL, fileURLToPath } from "node:url";

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const projectRoot = path.resolve(__dirname, "..", "..");
const docsRoot = path.join(projectRoot, "docs");
const outputPath = path.join(docsRoot, "generated-terraria-assets.js");
const localImageRoot = path.join(docsRoot, "assets", "images", "vanilla");

const { entries } = await import(pathToFileURL(path.join(docsRoot, "data.js")).href);
const { frontierEntries } = await import(pathToFileURL(path.join(docsRoot, "orespawn-data.js")).href);
const { generatedOreSpawnEntries } = await import(pathToFileURL(path.join(docsRoot, "generated-orespawn-data.js")).href);

const TERRARIA_WIKI = {
  apiUrl: "https://terraria.wiki.gg/api.php",
  pageBaseUrl: "https://terraria.wiki.gg/wiki/"
};

const WORKSTATIONS = [
  { label: "Lunar Crafting Station", keywords: ["lunar crafting station"] },
  { label: "Ancient Manipulator", keywords: ["ancient manipulator"] },
  { label: "Mythril Anvil", keywords: ["mythril anvil", "orichalcum anvil"] },
  { label: "Adamantite Forge", keywords: ["adamantite forge"] },
  { label: "Anvil", keywords: [" anvil", "anvil "] },
  { label: "Furnace", keywords: ["hellforge", "forge", "furnace"] }
];

const EXPLICIT_PAGE_ALIASES = {
  "lunar bars": "Luminite Bar",
  "lunar crafting station": "Ancient Manipulator"
};

const allEntries = [...entries, ...generatedOreSpawnEntries, ...frontierEntries];
const existingManifest = await loadExistingManifest();
const labels = collectLabels(allEntries);
const manifest = { ...existingManifest };
fs.mkdirSync(localImageRoot, { recursive: true });

for (const label of labels) {
  const asset = await fetchTerrariaWikiAsset(label);
  if (asset?.imageUrl) {
    asset.imageUrl = await cacheImageLocally(asset.imageUrl, asset.title || label);
    manifest[getKey(label)] = asset;
    console.log(`Resolved ${label}`);
  } else {
    console.log(`Missing ${label}`);
  }
  await sleep(250);
}

const banner = [
  "// Auto-generated by tools/wiki/generate-terraria-asset-manifest.mjs",
  "// This manifest avoids runtime CORS issues when resolving vanilla Terraria images.",
  ""
].join("\n");

fs.writeFileSync(outputPath, `${banner}export const generatedTerrariaAssets = ${JSON.stringify(manifest, null, 2)};\n`, "utf8");
console.log(`Generated ${Object.keys(manifest).length} Terraria asset entries at ${outputPath}`);

function collectLabels(entryList) {
  const set = new Set();

  for (const entry of entryList) {
    const meta = entry?.content?._meta ?? {};
    if (meta.vanillaAlias) {
      set.add(cleanLabel(meta.vanillaAlias));
    }
    if (meta.wikiSource) {
      set.add(cleanLabel(parseTerrariaWikiReference(meta.wikiSource)));
    }

    const content = entry?.content?.["pt-BR"] ?? entry?.content?.en;
    if (!content || Array.isArray(content)) {
      continue;
    }

    const lines = content.crafting ?? [];
    extractWorkstations(lines).forEach((station) => set.add(cleanLabel(station.label)));
    lines.forEach((line) => {
      const cleaned = stripStationClause(line);
      cleaned
        .split("+")
        .map((segment) => parseIngredientLabel(segment))
        .filter(Boolean)
        .forEach((ingredientLabel) => {
          if (!findEntryByMention(entryList, ingredientLabel)) {
            set.add(cleanLabel(ingredientLabel));
          }
        });
    });
  }

  return [...set].filter(Boolean).sort();
}

function parseIngredientLabel(segment) {
  const normalizedSegment = String(segment ?? "").includes(":")
    ? String(segment).split(":").pop().trim()
    : String(segment ?? "").trim();
  const suffixAmount = normalizedSegment.match(/^(.+?)\s+x(\d+(?:-\d+)?)$/i);
  const prefixAmount = normalizedSegment.match(/^(\d+(?:-\d+)?)\s+(.+)$/i);
  return (prefixAmount?.[2] ?? suffixAmount?.[1] ?? normalizedSegment).trim();
}

function extractWorkstations(lines) {
  const stations = [];
  const seen = new Set();

  lines.forEach((line) => {
    const normalizedLine = ` ${normalize(line)} `;
    WORKSTATIONS.forEach((station) => {
      if (station.label === "Anvil" && (normalizedLine.includes(" mythril anvil ") || normalizedLine.includes(" orichalcum anvil "))) {
        return;
      }

      if (!seen.has(station.label) && station.keywords.some((keyword) => normalizedLine.includes(keyword))) {
        seen.add(station.label);
        stations.push(station);
      }
    });
  });

  return stations;
}

function stripStationClause(line) {
  const trimmed = String(line ?? "").trim();
  if (!trimmed) {
    return "";
  }

  const stationOnly = extractWorkstations([trimmed]);
  if (stationOnly.length > 0 && !trimmed.includes("+") && !/\d/.test(trimmed)) {
    return "";
  }

  const matcher = trimmed.match(/^(.*?)(?:\s+(?:na|no|em|at)\s+.+)$/i);
  if (matcher && extractWorkstations([trimmed]).length > 0) {
    return matcher[1].trim();
  }

  return trimmed;
}

function findEntryByMention(entryList, label) {
  const normalizedLabel = normalize(label).replace(/^the\s+/, "").trim();
  if (!normalizedLabel) {
    return null;
  }

  const exact = entryList.find((entry) => {
    const meta = entry?.content?._meta ?? {};
    const title = entry?.content?.["pt-BR"]?.title ?? entry?.content?.en?.title ?? entry?.id ?? "";
    const alias = normalize(meta.vanillaAlias).trim();
    return normalize(entry.id) === normalizedLabel
      || normalize(title) === normalizedLabel
      || (alias && alias === normalizedLabel);
  });

  if (exact) {
    return exact;
  }

  return entryList.find((entry) => {
    const meta = entry?.content?._meta ?? {};
    const title = entry?.content?.["pt-BR"]?.title ?? entry?.content?.en?.title ?? entry?.id ?? "";
    const alias = normalize(meta.vanillaAlias).trim();
    return normalizedLabel.includes(normalize(title))
      || normalizedLabel.includes(normalize(entry.id))
      || (alias && normalizedLabel.includes(alias));
  }) ?? null;
}

async function fetchTerrariaWikiAsset(label) {
  const candidates = buildTerrariaWikiCandidates(label);

  for (const candidate of candidates) {
    const asset = await fetchTerrariaWikiPageAsset(candidate);
    if (asset?.imageUrl) {
      return asset;
    }
  }

  return null;
}

async function fetchTerrariaWikiParse(title) {
  const url = new URL(TERRARIA_WIKI.apiUrl);
  url.searchParams.set("action", "parse");
  url.searchParams.set("page", title);
  url.searchParams.set("prop", "text");
  url.searchParams.set("format", "json");
  const response = await fetchWithRetry(url);
  return response.ok ? response.json() : null;
}

async function fetchTerrariaWikiPageAsset(title) {
  const url = `${TERRARIA_WIKI.pageBaseUrl}${encodeURIComponent(title.replaceAll(" ", "_"))}`;
  const response = await fetchWithRetry(url);
  if (!response.ok) {
    return null;
  }

  const html = await response.text();
  const preferred = html.match(/class="infobox[^"]*"[\s\S]*?<img[^>]+alt="[^"]*sprite[^"]*"[^>]+src="([^"]+)"/i)?.[1]
    ?? html.match(/class="infobox[^"]*"[\s\S]*?<img[^>]+src="([^"]+)"/i)?.[1]
    ?? "";

  if (!preferred) {
    const parsed = await fetchTerrariaWikiParse(title);
    const fallbackImage = parsed?.parse?.text?.["*"] ? extractTerrariaWikiImage(parsed.parse.text["*"]) : "";
    if (!fallbackImage) {
      return null;
    }
    const finalTitle = decodeURIComponent(new URL(response.url).pathname.replace(/^\/wiki\//, "")).replaceAll("_", " ").trim();
    return {
      title: finalTitle || title,
      imageUrl: fallbackImage,
      pageUrl: response.url
    };
  }

  let imageUrl = preferred;
  if (preferred.startsWith("//")) {
    imageUrl = `https:${preferred}`;
  }
  else if (preferred.startsWith("/")) {
    imageUrl = `https://terraria.wiki.gg${preferred}`;
  }

  const finalTitle = decodeURIComponent(new URL(response.url).pathname.replace(/^\/wiki\//, "")).replaceAll("_", " ").trim();
  return {
    title: finalTitle || title,
    imageUrl,
    pageUrl: response.url
  };
}

function buildTerrariaWikiCandidates(label) {
  const clean = cleanLabel(parseTerrariaWikiReference(EXPLICIT_PAGE_ALIASES[getKey(label)] ?? label))
    .replace(/\s+x\d+(?:-\d+)?$/i, "")
    .replace(/^\d+(?:-\d+)?\s+/, "")
    .trim();
  const candidates = new Set([clean]);
  if (clean.endsWith("s")) candidates.add(clean.slice(0, -1));
  return [...candidates].filter(Boolean);
}

function parseTerrariaWikiReference(reference) {
  const raw = String(reference ?? "").trim();
  if (!raw) return "";
  if (/^https?:\/\//i.test(raw)) {
    const url = new URL(raw);
    if (!/terraria\.wiki\.gg$/i.test(url.hostname)) return "";
    return decodeURIComponent(url.pathname.replace(/^\/wiki\//, "")).replaceAll("_", " ").trim();
  }
  return raw.replaceAll("_", " ").trim();
}

function extractTerrariaWikiImage(html) {
  const match = html.match(/<img[^>]+src=\"([^\"]+)\"[^>]*>/i);
  const src = match?.[1] ?? "";
  if (!src) return "";
  if (src.startsWith("//")) return `https:${src}`;
  if (src.startsWith("/")) return `https://terraria.wiki.gg${src}`;
  return src;
}

function cleanLabel(value) {
  return String(value ?? "").replace(/\s+/g, " ").trim();
}

function normalize(value) {
  return String(value ?? "").toLowerCase();
}

function getKey(label) {
  return normalize(cleanLabel(label));
}

async function fetchWithRetry(url, attempts = 4) {
  for (let attempt = 0; attempt < attempts; attempt += 1) {
    const response = await fetch(url);
    if (response.status !== 429) {
      return response;
    }
    await sleep(800 * (attempt + 1));
  }
  return fetch(url);
}

function sleep(ms) {
  return new Promise((resolve) => setTimeout(resolve, ms));
}

async function cacheImageLocally(remoteUrl, title) {
  const cleanTitle = cleanLabel(title).replace(/[^\w\s-]+/g, "").replace(/\s+/g, "-").toLowerCase();
  const extension = path.extname(new URL(remoteUrl).pathname) || ".png";
  const fileName = `${cleanTitle}${extension}`;
  const destination = path.join(localImageRoot, fileName);

  if (!fs.existsSync(destination)) {
    const response = await fetchWithRetry(remoteUrl);
    if (!response.ok) {
      return remoteUrl;
    }
    const buffer = Buffer.from(await response.arrayBuffer());
    fs.writeFileSync(destination, buffer);
  }

  return `./assets/images/vanilla/${fileName}`;
}

async function loadExistingManifest() {
  if (!fs.existsSync(outputPath)) {
    return {};
  }

  try {
    const moduleUrl = `${pathToFileURL(outputPath).href}?ts=${Date.now()}`;
    const module = await import(moduleUrl);
    return module.generatedTerrariaAssets ?? {};
  }
  catch {
    return {};
  }
}
